package Chap03;

public class Example06 {
    public static void main(String[] args) {
        int i = 100; // 변수 i 선언 및 초기화
        long l = i; // int형 i를 long형 l로 변환
        float f = l; // long형 l을 float형 f로 변환
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
    }
}
