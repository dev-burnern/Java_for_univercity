package Chap03;

public class Example01 {
    public static void main(String[] args) {
        int x = 10,  y = 20; // 변수 x와 y 선언 및 초기화
        System.out.println("x + y = " + (x + y)); // x와 y의 합 출력
        System.out.println("x - y = " + (x - y)); // x와 y의 차 출력
        System.out.println("x * y = " + (x * y)); // x와 y의 곱 출력
        System.out.println("x / y = " + (x / y)); // x와 y의 몫 출력
        System.out.println("x % y = " + (x % y)); // x와 y의 나머지 출력
    }
}
