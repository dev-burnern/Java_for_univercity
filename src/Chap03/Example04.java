package Chap03;

public class Example04 {
    public static void main(String[] args) {
        int x = 10; // 변수 x 선언 및 초기화
        System.out.println(x); // x의 값 출력
        x += 2; // x에 2를 더함
        System.out.println(x); // x의 새로운 값 출력
        x *= 2; // x에 2를 곱함
        System.out.println(x); // x의 새로운 값 출력
        x -= 3; // x에서 3을 뺌
        System.out.println(x); // x의 새로운 값 출력
    }
}
