package Chap03;

public class Example05 {
    public static void main(String[] args) {
        int x = 10; // 변수 x 선언 및 초기화
        System.out.println(x++); // x의 현재 값 출력 후 1 증가
        System.out.println(++x); // x를 1 증가시킨 후 현재 값 출력
        System.out.println(x--); // x의 현재 값 출력 후 1 감소
        System.out.println(--x); // x를 1 감소시킨 후 현재 값 출력

    }
}
