package Chap02;

public class Datatype05 {
    public static void main(String[] args) {
        boolean t = true; // boolean 변수 t 선언 및 true로 초기화
        boolean f = false; // boolean 변수 f 선언 및 false로 초기화

        System.out.println("t = " + t); // 변수 t 출력
        System.out.println("f = " + f); // 변수 f 출력
    }
}
