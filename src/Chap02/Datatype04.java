package Chap02;

public class Datatype04 {
    public static void main(String[] args) {
        String str1 = "java"; // 문자열 변수 str1 선언 및 "java"로 초기화
        String str2 = "programming"; // 문자열 변수 str2 선언 및 "programming"으로 초기화

        System.out.println("str1 = " + str1); // 변수 str1 출력
        System.out.println("str2 = " + str2); // 변수 str2 출력
        System.out.println("str1 + str2 = " + str1 + str2); // 변수 str1과 str2의 값을 문자열로 연결하여 출력
    }
}
